    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Cliente c = new Cliente();
        DefaultTableModel grid = (DefaultTableModel) tbMelhoresClientes.getModel();

        while (grid.getRowCount() > 0) {
            grid.removeRow(0);
        }

        try {
            int i = 0;
            while (i < c.List().size()) {
                Cliente cli = (Cliente) c.List().get(i);

                grid.addRow(cli.carregarGrid());

                i++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar1ActionPerformed
        Cliente c = new Cliente();
        DefaultTableModel grid = (DefaultTableModel) tbMaisCompras.getModel();

        while (grid.getRowCount() > 0) {
            grid.removeRow(0);
        }

        try {
            int i = 0;
            while (i < c.List().size()) {

                Cliente cli = (Cliente) c.List().get(i);

                cli.setQtdCompra(c.quantidadeVendas(cli));

                grid.addRow(cli.carregarGridCompras());

                i++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnBuscar1ActionPerformed

