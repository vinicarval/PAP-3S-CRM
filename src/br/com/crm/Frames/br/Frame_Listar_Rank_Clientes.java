    private void btnAumentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAumentarActionPerformed
        Cliente c = new Cliente();
        Cliente cli = new Cliente();
        DefaultTableModel grid = (DefaultTableModel) tbMelhoresClientes.getModel();
        int selecionada = tbMelhoresClientes.getSelectedRow();

        if (selecionada != -1) {

            String cpf = tbMelhoresClientes.getValueAt(selecionada, 1).toString();
            String classificacao = tbMelhoresClientes.getValueAt(selecionada, 2).toString();

            try {

                cli = c.findCliente(cpf);

                if (classificacao.contains("1 - NOVO")) {
                    cli.setClassificacao("2 - REGULAR");
                    JOptionPane.showMessageDialog(this, "CLASSIFICAÇÃO ATUALIZADA");
                } else if (classificacao.contains("2 - REGULAR")) {
                    cli.setClassificacao("3 - IMPORTANTE");
                    JOptionPane.showMessageDialog(this, "CLASSIFICAÇÃO ATUALIZADA");
                } else if (classificacao.contains("3 - IMPORTANTE")) {
                    cli.setClassificacao("4 - MUITO IMPORTANTE");
                    JOptionPane.showMessageDialog(this, "CLASSIFICAÇÃO ATUALIZADA");
                } else if (classificacao.contains("4 - MUITO IMPORTANTE")) {
                    JOptionPane.showMessageDialog(this, "CLASSIFICAÇÃO NO MÁXIMO");

                }

                c.updateApenasCliente(cli);

                while (grid.getRowCount() > 0) {
                    grid.removeRow(0);
                }

                int i = 0;
                while (i < c.List().size()) {
                    Cliente clis = (Cliente) c.List().get(i);

                    grid.addRow(clis.carregarGrid());

                    i++;
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btnAumentarActionPerformed

